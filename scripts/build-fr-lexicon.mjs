#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";

const args = parseArgs(process.argv.slice(2));

if (!args.unimorph || !args.out) {
  console.error("Usage: node scripts/build-fr-lexicon.mjs --unimorph vendor/unimorph/fra --out data/fr-lexicon-index.js");
  process.exit(1);
}

const byLemma = new Map();
const byForm = new Map();

readUnimorph(args.unimorph);
writeIndex(args.out);

function readUnimorph(filePath) {
  const input = fs.readFileSync(filePath, "utf8");
  for (const [index, line] of input.split(/\r?\n/).entries()) {
    if (!line.trim() || line.startsWith("#")) continue;

    const [rawLemma, rawForm] = line.split("\t");
    const lemma = normalizeFrenchSurface(rawLemma);
    const form = normalizeFrenchSurface(rawForm);
    if (!lemma || !form) {
      console.warn(`Skipping malformed UniMorph line ${index + 1}: ${line}`);
      continue;
    }

    addToSetMap(byLemma, lemma, form);
    addToSetMap(byForm, form, lemma);
    addToSetMap(byForm, lemma, lemma);
  }
}

function writeIndex(filePath) {
  const index = {
    byForm: sortedObjectFromMap(byForm),
    byLemma: sortedObjectFromMap(byLemma)
  };
  const body = `// Generated by scripts/build-fr-lexicon.mjs. Do not edit by hand.\n` +
    `globalThis.FR_LEXICON_INDEX = ${JSON.stringify(index, null, 2)};\n`;

  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, body);
}

function parseArgs(values) {
  const parsed = {};
  for (let index = 0; index < values.length; index += 1) {
    const value = values[index];
    if (!value.startsWith("--")) continue;
    const key = value.slice(2);
    parsed[key] = values[index + 1];
    index += 1;
  }
  return parsed;
}

function addToSetMap(map, key, value) {
  if (!map.has(key)) map.set(key, new Set());
  map.get(key).add(value);
}

function sortedObjectFromMap(map) {
  return Object.fromEntries(
    [...map.entries()]
      .sort(([a], [b]) => a.localeCompare(b, "fr"))
      .map(([key, values]) => [key, [...values].sort((a, b) => a.localeCompare(b, "fr"))])
  );
}

function normalizeFrenchSurface(value) {
  return String(value || "")
    .trim()
    .replace(/[’‘]/g, "'")
    .replace(/\s+/g, " ")
    .toLowerCase();
}
